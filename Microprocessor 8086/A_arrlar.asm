DATA SEGMENT
	ARR DB 1,4,2,3,9,8,6,7,5,10
	LEN DW $-ARR
	LAR DB ?
DATA ENDS
CODE SEGMENT
ASSUME DS:DATA,CS:CODE
START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,ARR
	CLC
	MOV AL,ARR[SI]
	MOV LAR,AL
	MOV CX,LEN
	REPEAT:
	MOV AL,ARR[SI]
	CMP LAR,AL
	JG NOCHANGE
	MOV LAR,AL
	NOCHANGE:
	INC SI
	LOOP REPEAT
	MOV AH,00H
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
